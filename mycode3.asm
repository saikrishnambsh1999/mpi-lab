.MODEL SMALL
ASSUME CS:CODE,DS:DATA
DATA SEGMENT
NUM DB 0FCH
STR1 DW "Positive$"
STR2 DW "Negative$"
DATA ENDS
CODE SEGMENT
MOV AX,DATA
MOV DS,AX
XOR AX,AX
MOV AL,NUM    
ROL AL,1
JC NEXT
JNC MOVE
NEXT:
    MOV DX,OFFSET STR2
    JMP EXIT

MOVE:
    MOV DX,OFFSET STR1
    
EXIT:    
MOV AH,09H        
INT 21H
CODE ENDS